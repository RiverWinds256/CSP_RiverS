print("welcome to the are you old enough calculator")

age=float(input("How old are you? "))

if age >=18:    
    print("You are old enough to go to school, get a learner permit, drive and vote.")

elif age >=16: 
     print ("You are old enough to go to school, get a learner permit, drive, but not vote")    

elif age >=15:
     print ("You are old enough to go to school, get a learner permit, but not drive and vote.")

elif age >5:
    print ("you are old enough to go to school, but not old enough get a learners permit, drive, or vote.")  
   
else:
     print("you are not old enough go to school, get a learners permit, drive, or vote")     